export function reflectionForToday(date = new Date()): Reflection {
  const dayIndex = Math.floor(
    (date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86_400_000,
  );
  return seedReflections[dayIndex % seedReflections.length];
}
